
public class Carta {
    private Integer id;
    private Boolean mico = false;
    
    public Carta(){
        
    }

    /**
     * @return the id
     */
    public Integer getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(Integer id) {
        this.id = id;
    }

    /**
     * @return the mico
     */
    public Boolean getMico() {
        return mico;
    }

    /**
     * @param mico the mico to set
     */
    public void setMico(Boolean mico) {
        this.mico = mico;
    }
}
